    private void btnAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccionActionPerformed
        try {
            int edifID = Integer.parseInt(txtEdifID.getText());
            String nombreEdif = txtEdifName.getText();
            String calle = txtCalle.getText();
            int numero = Integer.parseInt(txtNumero.getText());
            String barrio = txtBarrio.getText();
            String propietario = txtPropietario.getText();
            String inquilino = txtInquilino.getText();
            int piso = Integer.parseInt(txtPiso.getText());
            String letra = txtLetra.getText();
            int gastos = Integer.parseInt(txtGastos.getText());
            
            departamentosObj = new departamentos(edifID, nombreEdif, calle, numero, barrio, null, propietario, inquilino, piso, letra, gastos);
            deptoCtrl.insertar(departamentosObj);
            JOptionPane.showMessageDialog(null, "Funcionario registrado con éxito!");
            
            this.actualizar();
        }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID Edificio, número, piso y gastos deben ser numéricos!");
        }


    }//GEN-LAST:event_btnAccionActionPerformed

    private void jmiModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiModificarActionPerformed
        Integer filaNum = tblListado.getSelectedRow();
        if (filaNum < 0) {
        JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        } else {
        DefaultTableModel modelo = (DefaultTableModel) tblListado.getModel();
        Integer deptoID = (Integer) modelo.getValueAt(filaNum, 6);
        String whereTMP = "WHERE id_depto='"+deptoID+"'";
        departamentosLista = deptoCtrl.listar(whereTMP);
        departamentosObj = departamentosLista.get(6);
        
        ;
        
        }
    }//GEN-LAST:event_jmiModificarActionPerformed


    public void actualizar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Edificio");
        modelo.addColumn("Nombre");
        modelo.addColumn("Calle");
        modelo.addColumn("Numero");
        modelo.addColumn("Barrio");
        modelo.addColumn("ID Depto");
        modelo.addColumn("Propietario");
        modelo.addColumn("Inquilino");
        modelo.addColumn("Piso");
        modelo.addColumn("Letra");
        modelo.addColumn("Gastos");

        departamentosLista = deptoCtrl.listar("");
        Object[] fila = new Object[11];

        for (int i = 0; i < departamentosLista.size(); i++) {
            departamentosObj = departamentosLista.get(i);
            fila[0] = departamentosObj.getEdifID();
            fila[1] = departamentosObj.getNombre();
            fila[2] = departamentosObj.getCalle();
            fila[3] = departamentosObj.getNumero();
            fila[4] = departamentosObj.getBarrio();
            fila[5] = departamentosObj.getDeptoID();
            fila[6] = departamentosObj.getPropietario();
            fila[7] = departamentosObj.getInquilino();
            fila[8] = departamentosObj.getPiso();
            fila[9] = departamentosObj.getLetra();
            fila[10] = departamentosObj.getGastos();
            modelo.addRow(fila);

        }
        tblListado.setModel(modelo);
    }

